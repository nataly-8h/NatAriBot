import java.awt.Color;

public class Caja {
	private int x,
				y,
				color;
	public Caja(int color) {
		this.x = 0;
		this.y = 0;
		this.color = color;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
